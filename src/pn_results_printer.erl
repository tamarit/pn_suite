print_latex(Dict) ->
    [{_, DictProp} | _] = 
        dict:to_list(Dict),
    Props = 
        dict:fetch_keys(DictProp),
    Algorithms = [  "Llorens et al.'s slicer (original)",
                    "Llorens et al.'s slicer", 
                    "Llorens et al.'s slicer (precise)", 
                    "Rakow's slicer CTL", 
                    "Rakow's slicer safety", 
                    "Yu et al.'s slicer"],
    io:format("$Property$" ++ lists:concat([" & " ++ A|| A <- Algorithms]) ++  " \\\n"),
    lists:map(
        fun(P) ->
            case lists:member(P, ["num_places", "num_transitions", "num_arcs", "num_tokens", "size", "time"]) of 
                true -> 
                    Values = 
                        dict:fold(
                            fun (K, DictAlg, Acc) ->
                                {V, C} = dict:fetch(P, DictAlg),
                                case {K, P} of
                                    {"Llorens et al.'s slicer (precise)", "time"} ->
                                        % C: 100%
                                        % Python: 692% (118% with PyPy)
                                        % Erlang: 436% (135% thanks to RichardC)
                                        % Haskell: 1421% 
                                        % Correct Python times according this observations. 
                                        % Erlang is 5,13x faster
                                        % As it is used in half of the algorithm, we will assume that each 6,13 s are distributed as 5,13 python and 1 erlang. We consider that 6,13 are 2 seconds. So we are dividind by 3,065
                                        [{K, {(V/3.065) /C, C}} | Acc];
                                    _ -> 
                                        [{K, {V/C, C}} | Acc]
                                end
                            end,
                            [],
                            Dict),
                    % io:format("Values: ~p\n", [Values]),
                    MaxFun = 
                        case lists:member(P, ["num_places", "num_transitions", "num_arcs", "num_tokens", "size", "time"]) of 
                            true -> 
                                fun lists:min/1;
                            false -> 
                                fun lists:max/1
                        end,
                    Max = 
                        MaxFun([V || {_, {V, _}} <- Values]),
                    DictValues = dict:from_list(Values),
                    io:format("$~s$", [replace_chars(P)]),
                    [begin 
                        {V, _} = dict:fetch(A, DictValues),
                        case P of 
                            "time" -> 
                                case V of 
                                    Max -> 
                                        io:format(" & \\textbf{~.2f}", [V]);
                                    _ ->
                                        io:format(" & ~.2f", [V])
                                end;
                            _ ->
                                case V of 
                                    Max -> 
                                        io:format(" & \\textbf{~.2f%}", [V*100]);
                                    _ ->
                                        io:format(" & ~.2f%", [V*100])
                                end
                        end
                    end
                    || A <- Algorithms
                    ],
                    io:format(" \\\\ \n");
                false ->
                    0
                end
        end,
        Props).

replace_chars([$_|Rest]) -> 
    [$\\,$_ | replace_chars(Rest)];
replace_chars([Other|Rest]) -> 
    [Other | replace_chars(Rest)];
replace_chars([]) -> 
    [].

